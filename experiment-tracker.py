#!/usr/bin/env python3
"""
experiment-tracker.py - Track and visualize Karpathy autoresearch experiments
Created for Eric - monitors training progress and generates reports
Usage: python3 experiment-tracker.py [--watch] [--report]
"""

import json
import sys
import time
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional, Tuple

WORKSPACE = Path("/root/.openclaw/workspace")
AUTORESEARCH_DIR = WORKSPACE / "autoresearch"
RESULTS_FILE = AUTORESEARCH_DIR / "results.tsv"
REPORT_FILE = WORKSPACE / "memory" / "experiment-reports.md"


def parse_results_tsv() -> List[Dict]:
    """Parse the results.tsv file for experiment data."""
    if not RESULTS_FILE.exists():
        return []
    
    experiments = []
    lines = RESULTS_FILE.read_text().strip().split('\n')
    
    if len(lines) < 2:
        return []
    
    # Parse header
    headers = lines[0].split('\t')
    
    # Parse data rows
    for line in lines[1:]:
        values = line.split('\t')
        if len(values) == len(headers):
            exp = dict(zip(headers, values))
            experiments.append(exp)
    
    return experiments


def analyze_experiments(experiments: List[Dict]) -> Dict:
    """Analyze experiment results and compute statistics."""
    if not experiments:
        return {
            "total": 0,
            "best_val_bpb": None,
            "best_params": None,
            "improvement": 0,
            "all_experiments": [],
        }
    
    # Find best validation BPB (bits per byte - lower is better)
    best_exp = None
    best_bpb = float('inf')
    
    for exp in experiments:
        try:
            val_bpb = float(exp.get('val_bpb', float('inf')))
            if val_bpb < best_bpb:
                best_bpb = val_bpb
                best_exp = exp
        except (ValueError, TypeError):
            continue
    
    # Calculate improvement if we have baseline
    baseline = float(experiments[0].get('val_bpb', best_bpb)) if experiments else best_bpb
    improvement = ((baseline - best_bpb) / baseline * 100) if baseline > 0 else 0
    
    return {
        "total": len(experiments),
        "best_val_bpb": best_bpb if best_bpb != float('inf') else None,
        "best_params": best_exp,
        "improvement": improvement,
        "all_experiments": experiments
    }


def generate_report(analysis: Dict) -> str:
    """Generate a formatted report."""
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    lines = [
        "# 🔬 AutoResearch Experiment Report",
        f"**Generated:** {now}",
        "",
        "## 📊 Summary",
        "",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Total Experiments | {analysis['total']} |",
    ]
    
    if analysis['best_val_bpb'] is not None:
        lines.append(f"| Best Val BPB | {analysis['best_val_bpb']:.6f} |")
        lines.append(f"| Improvement | +{analysis['improvement']:.2f}% |")
    else:
        lines.append(f"| Best Val BPB | N/A |")
        lines.append(f"| Improvement | N/A |")
    
    lines.extend([
        "",
        "## 🏆 Best Configuration",
        "",
    ])
    
    if analysis['best_params']:
        params = analysis['best_params']
        lines.append("```json")
        lines.append(json.dumps(params, indent=2))
        lines.append("```")
    else:
        lines.append("*No valid experiments found yet*")
    
    lines.extend([
        "",
        "## 📈 All Experiments",
        "",
    ])
    
    if analysis['all_experiments']:
        lines.append("| # | Val BPB | Notes |")
        lines.append("|---|---------|-------|")
        for i, exp in enumerate(analysis['all_experiments'][-10:], 1):  # Last 10
            val_bpb = exp.get('val_bpb', 'N/A')
            notes = exp.get('notes', '')[:30]
            lines.append(f"| {i} | {val_bpb} | {notes} |")
    
    lines.extend([
        "",
        "---",
        "*Auto-generated by experiment-tracker.py*",
    ])
    
    return '\n'.join(lines)


def print_console_summary(analysis: Dict):
    """Print a quick console summary."""
    print("="*50)
    print("🔬 AutoResearch Experiment Tracker")
    print("="*50)
    print(f"Total experiments: {analysis['total']}")
    
    if analysis['best_val_bpb'] is not None:
        print(f"Best Val BPB: {analysis['best_val_bpb']:.6f}")
        print(f"Improvement: +{analysis['improvement']:.2f}%")
        print("")
        print("🏆 Best params so far:")
        if analysis['best_params']:
            for key, value in list(analysis['best_params'].items())[:5]:
                print(f"  {key}: {value}")
    else:
        print("No valid results yet. Waiting for experiments...")
    
    print("="*50)


def watch_mode():
    """Continuously watch for new results."""
    print("🔍 Watching for new experiments (Ctrl+C to stop)...")
    print("")
    
    last_count = 0
    
    try:
        while True:
            experiments = parse_results_tsv()
            
            if len(experiments) != last_count:
                last_count = len(experiments)
                analysis = analyze_experiments(experiments)
                print_console_summary(analysis)
                print(f"\n⏱️  Last update: {datetime.now().strftime('%H:%M:%S')}")
                print("-"*50)
            
            time.sleep(10)  # Check every 10 seconds
            
    except KeyboardInterrupt:
        print("\n\n👋 Stopping watch mode")


def save_report(report: str):
    """Save report to file."""
    REPORT_FILE.parent.mkdir(parents=True, exist_ok=True)
    REPORT_FILE.write_text(report)
    print(f"💾 Report saved to: {REPORT_FILE}")


def main():
    """Main entry point."""
    if "--watch" in sys.argv:
        watch_mode()
        return
    
    # Parse and analyze
    experiments = parse_results_tsv()
    analysis = analyze_experiments(experiments)
    
    # Print console summary
    print_console_summary(analysis)
    
    # Generate and save report
    report = generate_report(analysis)
    
    if "--report" in sys.argv:
        save_report(report)
        print(f"\n📄 Full report generated")
    
    print("\n💡 Usage:")
    print("  python3 experiment-tracker.py --watch    # Watch mode")
    print("  python3 experiment-tracker.py --report   # Save full report")


if __name__ == "__main__":
    main()
